<script type="text/javascript">
// Utility helper to read cookie variables by name strings safely
function getTranslationCookie(name) {
    let value = "; " + document.cookie;
    let parts = value.split("; " + name + "=");
    if (parts.length === 2) return parts.pop().split(";").shift();
    return null;
}

// Google initialization pipeline execution hook
function googleTranslateElementInit() {
    new google.translate.TranslateElement({
        pageLanguage: 'en',
        layout: google.translate.TranslateElement.InlineLayout.SIMPLE,
        autoDisplay: false
    }, 'google_translate_element');

    // Run custom persistence verification layer loop
    setTimeout(function() {
        const savedLang = getTranslationCookie("goog_lang");
        if (savedLang && savedLang !== 'en') {
            const googleCombo = document.querySelector('.goog-te-combo');
            if (googleCombo) {
                googleCombo.value = savedLang;
                googleCombo.dispatchEvent(new Event('change'));
            }
        }
    }, 600); // 600ms latency window ensures API script completely populates DOM nodes securely
}
</script>
